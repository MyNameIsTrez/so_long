////////////////////////////////////////////////////////////////////////////////

#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

void	bounce_high(t_i32 *step_ptr, t_u8 *channel, t_u8 min_channel,
			t_u8 max_channel)
{
	*step_ptr -= max_channel - *channel;
	if (max_channel - *step_ptr >= min_channel)
		*channel = (t_u8)(max_channel - *step_ptr);
	*step_ptr *= -1;
}

