t_i32	*get_channel_step_ptr(size_t rgb_channel_index, t_data *data)
{
	t_tile_kind	*tile_kind;
	t_i32		*rgb_step;

	tile_kind = data->it.tile_kind;
	rgb_step = tile_kind->color.step;
	return (&rgb_step[rgb_channel_index]);
}

