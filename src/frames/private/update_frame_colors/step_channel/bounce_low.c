#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

void	bounce_low(t_i32 *step_ptr, t_u8 *channel, t_u8 min_channel,
			t_u8 max_channel)
{
	*step_ptr += *channel - min_channel;
	if (min_channel - *step_ptr <= max_channel)
		*channel = (t_u8)(min_channel - *step_ptr);
	*step_ptr *= -1;
}

