#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

t_i32	sl_get_heading_row(t_heading heading, t_tile *tile, t_data *data)
{
	t_i32	row;

	row = (t_i32)sl_get_tile_row(tile, data);
	if (heading == HEADING_UP)
		return (row - 1);
	if (heading == HEADING_DOWN)
		return (row + 1);
	return (row);
}

