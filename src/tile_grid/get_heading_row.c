#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

t_i32	sl_get_heading_row(t_heading heading, t_tile *tile)
{
	if (heading == HEADING_UP)
		return ((t_i32)tile->row_index - 1);
	if (heading == HEADING_DOWN)
		return ((t_i32)tile->row_index + 1);
	else
		return ((t_i32)tile->row_index);
}

