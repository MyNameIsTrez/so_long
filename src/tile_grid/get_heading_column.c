#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

t_i32	get_heading_column(t_heading heading, t_tile *tile)
{
	if (heading == HEADING_LEFT)
		return ((t_i32)tile->column_index - 1);
	if (heading == HEADING_RIGHT)
		return ((t_i32)tile->column_index + 1);
	else
		return ((t_i32)tile->column_index);
}

