#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

void	sl_mlx_cleanup(t_data *data)
{
	if (data->texture.data != NULL)
		mlx_delete_texture(data->texture.data);
	if (data->mlx != NULL)
		mlx_terminate(data->mlx);
}

