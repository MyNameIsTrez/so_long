#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

t_i32	get_heading_row_offset(t_heading heading)
{
	if (heading == HEADING_UP)
		return (-1);
	if (heading == HEADING_DOWN)
		return (1);
	else
		return (0);
}

