t_status	get_str_pixels_per_tile(char **str_pixels_per_tile, t_i32 scale)
{
	*str_pixels_per_tile = ft_itoa(scale);
	if (*str_pixels_per_tile == NULL)
		return (ft_any_error());
	return (OK);
}

