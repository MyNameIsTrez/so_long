#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

t_status	sl_set_real_scale(t_i32 scale, t_data *data)
{
	static const size_t	valid_pixels_per_tile[] = {
	[1] = 1,
	[2] = 5,
	[3] = 10,
	[4] = 20,
	};
	t_texture			*texture;

	texture = &data->texture;
	texture->scale = valid_pixels_per_tile[scale];
	return (OK);
}

