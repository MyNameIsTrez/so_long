#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

void	sl_open_exit_locks(t_data *data)
{
	t_iterator	it;
	t_exit_lock	*exit_lock;

	ft_init_it(&it);
	while (sl_iterate_exit_locks(&it, data) != FINISHED)
	{
		exit_lock = data->it.exit_lock;
		sl_hide_and_disable_entity(exit_lock->entity);
	}
}

