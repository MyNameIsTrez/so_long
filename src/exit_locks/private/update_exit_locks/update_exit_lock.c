#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

void	sl_update_exit_lock(t_exit_lock *exit_lock, t_data *data)
{
	t_tile	*tile;

	(void)data;
	tile = &exit_lock->entity->tile;
	if (tile->frame_index == tile->tile_kind->frame_count - 1)
		sl_hide_and_disable_entity(exit_lock->entity);
}

