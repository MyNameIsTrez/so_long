#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

void	init_window_width(size_t pixels_per_tile, t_data *data)
{
	data->window.width = data->char_grid.width * pixels_per_tile;
	if (data->window.width > data->monitor.width)
		data->window.width = data->monitor.width;
}

