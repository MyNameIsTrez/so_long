bool	sl_out_of_bounds(int32_t column, int32_t row, t_data *data)
{
	if (column < 0 || column >= (int32_t)data->char_grid.width)
		return (true);
	if (row < 0 || row >= (int32_t)data->char_grid.height)
		return (true);
	return (false);
}

