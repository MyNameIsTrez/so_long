#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

bool	can_walk(t_i32 column, t_i32 row, t_data *data)
{
	return (!sl_out_of_bounds(column, row, data)
		&& is_tile_walkable((size_t)column, (size_t)row, data)
		&& !is_entity_in_way((size_t)column, (size_t)row, data));
}

