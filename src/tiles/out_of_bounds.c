#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

bool	sl_out_of_bounds(t_i32 column, t_i32 row, t_data *data)
{
	if (column < 0 || column >= (t_i32)data->char_grid.width)
		return (true);
	if (row < 0 || row >= (t_i32)data->char_grid.height)
		return (true);
	return (false);
}

