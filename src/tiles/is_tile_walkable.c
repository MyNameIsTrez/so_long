////////////////////////////////////////////////////////////////////////////////

bool	sl_is_tile_walkable(size_t index, t_data *data)
{
	t_u8	tile_character;

	tile_character = sl_get_tile_grid_character(index, data);
	return (ft_chr_in_str(tile_character, WALKABLE_CHARACTERS));
}

