bool	row_is_not_enclosed(size_t row, t_data *data)
{
	size_t	column;

	column = 0;
	while (column < data->char_grid.width)
	{
		if (cell_is_not_wall(column, row, data))
			return (true);
		column++;
	}
	return (false);
}

