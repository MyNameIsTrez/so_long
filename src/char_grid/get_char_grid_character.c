#include "so_long.h"

////////////////////////////////////////////////////////////////////////////////

t_u8	sl_get_char_grid_character(t_data *data)
{
	size_t	row;
	size_t	column;

	row = data->it.row;
	column = data->it.column;
	return ((t_u8)data->char_grid.cells[row][column]);
}

