void	sl_update_held_keys(t_data *data)
{
	while (sl_iterate_keys(data) != FINISHED)
	{
		if (mlx_is_key_down(data->mlx, data->t.key))
			data->held_keys[data->t.key]++;
		else if (data->held_keys[data->t.key] > 0)
			data->held_keys[data->t.key] = 0;
	}
}

