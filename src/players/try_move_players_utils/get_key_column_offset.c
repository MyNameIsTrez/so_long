t_i32	get_key_column_offset(keys_t key, keys_t movement_keys[4])
{
	if (key == movement_keys[LEFT_MOVEMENT_KEY_INDEX])
		return (-1);
	if (key == movement_keys[RIGHT_MOVEMENT_KEY_INDEX])
		return (1);
	else
		return (0);
}

