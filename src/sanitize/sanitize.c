#include "utils/sl_sanitize_utils.h"

////////////////////////////////////////////////////////////////////////////////

t_status	sl_sanitize(t_i32 argc, char **argv, t_data *data)
{
	if (sanitize_defines(data) != OK)
		return (sl_any_error());
	if (sanitize_argc(argc) != OK)
		return (sl_any_error());
	if (sanitize_argv(argc, argv, data) != OK)
		return (sl_any_error());
	return (OK);
}

