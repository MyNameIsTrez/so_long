////////////////////////////////////////////////////////////////////////////////

#include "private/sanitize/sl_private_sanitize.h"

////////////////////////////////////////////////////////////////////////////////

t_status	sl_sanitize(t_i32 argc, char **argv, t_data *data)
{
	if (sanitize_defines(data) != OK)
		return (ERROR);
	if (sanitize_argc(argc) != OK)
		return (ERROR);
	if (sanitize_argv(argc, argv) != OK)
		return (ERROR);
	return (OK);
}

