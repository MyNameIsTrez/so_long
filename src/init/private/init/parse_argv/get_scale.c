t_i32	get_scale(t_i32 argc, char **argv)
{
	char	*scale_string;
	t_i32	scale;

	if (argc >= 3)
	{
		scale_string = argv[2];
		ft_atoi_safe(scale_string, &scale);
	}
	else
		scale = DEFAULT_SCALE;
	return (scale);
}

